package com.bit.common;

public class userInfoTable {
	private String user_id;
	private String password;
	private String user_type;
	private String user_row;
	private String user_city;
	private String administor;
	public String getUser_id() {
		return user_id;
	}
	public String getUser_city() {
		return user_city;
	}
	public void setUser_city(String user_city) {
		this.user_city = user_city;
	}
	public String getAdministor() {
		return administor;
	}
	public void setAdministor(String administor) {
		this.administor = administor;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUser_type() {
		return user_type;
	}
	public void setUser_type(String user_type) {
		this.user_type = user_type;
	}
	public String getUser_row() {
		return user_row;
	}
	public void setUser_row(String user_row) {
		this.user_row = user_row;
	}
	
	
	
}
